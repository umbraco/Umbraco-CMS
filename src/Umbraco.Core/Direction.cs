namespace Umbraco.Cms.Core;

/// <summary>
///     Defines the direction for sorting operations.
/// </summary>
public enum Direction
{
    /// <summary>
    ///     Ascending sort order (A to Z, 0 to 9).
    /// </summary>
    Ascending = 0,

    /// <summary>
    ///     Descending sort order (Z to A, 9 to 0).
    /// </summary>
    Descending = 1,
}
