import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const clientDir = resolve(here, '..');
const backofficeDir = resolve(clientDir, '../../Umbraco.Web.UI.Client');
const tsconfigPath = resolve(clientDir, 'tsconfig.json');

const tsconfigComment = `
/* -------------------------------------------------------------------------


DON'T EDIT THIS FILE DIRECTLY. It is generated by /scripts/generate-tsconfig.js


--------------------------------------------------------------------------- */
`;

const backofficePkg = JSON.parse(readFileSync(resolve(backofficeDir, 'package.json'), 'utf8'));

// Cross-package imports into the Core Client sources (resolved via the runtime importmap).
const paths = {
	'@umbraco-cms/search/global': ['../../Umbraco.Cms.Search.Core.Client/Client/src/global/index.ts'],
	'@umbraco-cms/search/settings': ['../../Umbraco.Cms.Search.Core.Client/Client/src/settings/index.ts'],
};

// Map every @umbraco-cms/backoffice/<sub> import to the in-repo backoffice sources.
for (const [subpath, target] of Object.entries(backofficePkg.exports ?? {})) {
	if (target === null) continue; // The `.` self-reference.
	if (!subpath.startsWith('./')) continue;
	if (typeof target !== 'string') {
		throw new Error(
			`Unsupported exports entry for "${subpath}" in ${backofficePkg.name}: expected a string target, got ${typeof target}. ` +
				`Update the generator to handle conditional exports.`,
		);
	}

	const aliasKey = `${backofficePkg.name}/${subpath.slice(2)}`;
	const sourceTarget = target
		.replace(/^\.\/dist-cms\//, '../../Umbraco.Web.UI.Client/src/')
		.replace(/\.d\.ts$/, '.ts')
		.replace(/\.js$/, '.ts');
	paths[aliasKey] = [sourceTarget];
}

const tsConfig = {
	extends: './tsconfig.base.json',
	compilerOptions: {
		paths,
	},
	include: ['src'],
};

const content = tsconfigComment + JSON.stringify(tsConfig, null, '\t') + '\n';
const existing = existsSync(tsconfigPath) ? readFileSync(tsconfigPath, 'utf8') : null;

if (existing === content) {
	console.log(`tsconfig.json already up to date (${Object.keys(paths).length} path aliases)`);
} else {
	writeFileSync(tsconfigPath, content);
	console.log(`Wrote ${Object.keys(paths).length} path aliases to ${tsconfigPath}`);
}
